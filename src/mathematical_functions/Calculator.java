    private void btn_divActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_divActionPerformed
        // TODO add your handling code here:
        try {
            if (lbl_ans.getText().equalsIgnoreCase("ANS")) {
                lbl_ans.setText(txt_display.getText());
                txt_display.setText("");
            } else {
                result = Double.parseDouble(lbl_ans.getText()) / Double.parseDouble(txt_display.getText());
                lbl_ans.setText(String.valueOf(result));
                txt_display.setText("");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Syntax ERROR\n" + e.toString(), "Error - Project Group 14", JOptionPane.ERROR_MESSAGE);
            txt_display.setText("");
            lbl_ans.setText("ANS");
            result = 0.0;
        }
    }//GEN-LAST:event_btn_divActionPerformed

